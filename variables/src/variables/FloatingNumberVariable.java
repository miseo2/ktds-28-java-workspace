package variables;

public class FloatingNumberVariable {
	
	public static void main (String[] args) {
		
		//실수 (float, double)
		float floatNumber = 3.0f;
		float floatNumber1 = 3;
		float floatNumber2 = 3.441231242343544f;
		double doubleNumber = 3.1234567890123456789;
		
		System.out.println(floatNumber);
		System.out.println(floatNumber1);
		System.out.println(floatNumber2);
		System.out.println(doubleNumber);
		
	}

}
